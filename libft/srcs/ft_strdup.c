#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		len;
	int		i;
	char	*copy;

	len = ft_strlen(s1);
	if (!(copy = ft_strnew(len)))
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		copy[i] = s1[i];
		i++;
	}
	return (copy);
}
